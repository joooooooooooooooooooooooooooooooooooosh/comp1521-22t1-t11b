#include <stdio.h>
#include <stdlib.h>
#include <stdint.h>

int main(int argc, char **argv) {
    // 1  = 0b0000 0000 ... 0001
    // -1 = 0b1111 1111 ... 1111
    printf("0x%08x\n", 1 );
    printf("0x%08x\n", -1);

    // 2  = 0b0000 0000 ... 0010
    // -2 = 0b1111 1111 ... 1110
    printf("0x%08x\n", 2 );
    printf("0x%08x\n", -2);

    // two's complement: flip all bits and add one
    // 1 -> -1
    // 1  = 0b0000 0000 ... 0001
    //~1  = 0b1111 1111 ... 1110
    //-1  = 0b1111 1111 ... 1111 // add one
    //
    // -1 -> 1
    //-1  = 0b1111 1111 ... 1111
    //~-1 = 0b0000 0000 ... 0000
    // 1  = 0b0000 0000 ... 0001 // add one
    //
    // why isn't -1 0b1000 0000 ... 0001?
    //
    // 5 + -3 = 2
    // our idea:
    // 5  = 0b0000 0000 ... 0101
    // -3 = 0b1000 0000 ... 0011
    // 2  = 0b0000 0000 ... 0010
    //
    // 2's complement:
    // 5  = 0b0000 0000 ... 0101
    // -3 = 0b1111 1111 ... 1101
    //                
    // addition:
    // 0b 0000 0000 ... 0101
    // +  1111 1111 ... 1101
    //    0000 0000     0010

    return EXIT_SUCCESS;
}

