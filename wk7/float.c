// evaluate floats
// sign * (1 + frac) * 2^(exp - 127)
// sign = 1 if first bit is 0
//      = -1 if first bit is 1
//
// 0 00000000 00000000000000000000000
// 1 * (1 + 0) * 2^(0 - 127)
// 2^-127 ~= 0
//
// 1 00000000 00000000000000000000000
// -1 * (1 + 0) * 2^(0 - 127)
// -2^-127 ~= -0
//
// 0 01111111 10000000000000000000000
// 1   127         1/2 = 0.5
//
// 1 * (1 + 0.5) * 2^(127 - 127)
// 1.5 * 2^0 = 1.5
//
// 0 01111110 00000000000000000000000
// 1   126         0
// 1 * (1 + 0) * 2^(126 - 127)
// 2^-1 = 0.5
//
// 0 01111110 11111111111111111111111
// 1   126       0.9999998807907104
// 1 * (1 + 0.9999998807907104) * 2^(126 - 127)
// 1.9999998807907104 * 2^-1
// ~= 2 * 1/2 = 1
//
// 2.5
// sign     exp         frac
// 1        (127 + 1)   0.25
// 1 * (1 + 0.25) * 2^(128 - 127)
// 0 10000000 01000000000000000000000
//
// 0.375
// sign    exp         frac
// 1       (127 - 2)   0.5 = 1/2
// 1 * (1 + 0.5) * 2^(125 - 127)
// 0 01111101 10000000000000000000000
//
// 27.0
// sign    exp          frac
// 1       (127 + 4)     0.6875 = 1/2 + 1/8 + 1/16
